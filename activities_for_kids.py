def run(*names):
  for name in names:
    print(f"{name} ran like a fool!")


def swing(*names):
  for name in names:
    print(f"{name} swung like a fool!")


def slide(*names):
  for name in names:
    print(f"{name} slid like a fool!")


def hide_and_seek(*names):
  for name in names:
    print(f"{name} hid like a fool!")


run("Pam", "Sam", "Andrea", "Will")
swing("Marybeth", "Ariel", "Kevin", "Courtney")
slide("Mike", "Jack", "Jennifer", "Earl")
hide_and_seek("Henry", "Heather", "Hayley", "Hugh")